package beans;

public class ImagenDTO {
	
	private int ancho;
	private int alto;
	private int ladox;
	private int ladoy;
	private String nombre;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getAncho() {
		return ancho;
	}
	public void setAncho(int ancho) {
		this.ancho = ancho;
	}
	public int getAlto() {
		return alto;
	}
	public void setAlto(int alto) {
		this.alto = alto;
	}
	public int getLadox() {
		return ladox;
	}
	public void setLadox(int ladox) {
		this.ladox = ladox;
	}
	public int getLadoy() {
		return ladoy;
	}
	public void setLadoy(int ladoy) {
		this.ladoy = ladoy;
	}
	
	

}
